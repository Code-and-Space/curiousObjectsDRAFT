function rotateSakeenaCircle(incomingsakeenaCircle) {
  push();

  // In here, you start manipulating the initial start point.
  // I adjusted it so that over each for loop, translateMultiplier gets smaller and smaller. This way, eventually, all clusters return to it's base origin.
  let translatePt = incomingsakeenaCircle.translatePtMultiplier*i;
  if (incomingsakeenaCircle.translatePtMultiplier > 0) {
    incomingsakeenaCircle.translatePtMultiplier = incomingsakeenaCircle.translatePtMultiplier-sakeenaSpeedDial;
    // console.log(incomingsakeenaCircle.translatePtMultiplier)
  }
  translate(translatePt, translatePt);

  // In here, you start manipulating the initial angle of rotation.
  // I adjusted it so that over each for loop, translateAngleMultiplier gets smaller and smaller. This way, eventually, all clusters return to it's base origin.
  rotate(incomingsakeenaCircle.translateAngleMultiplier*i);
  if (incomingsakeenaCircle.translateAngleMultiplier > 0) {
    incomingsakeenaCircle.translateAngleMultiplier = incomingsakeenaCircle.translateAngleMultiplier-sakeenaSpeedDial;
    // console.log(incomingsakeenaCircle.translateAngleMultiplier)
  }

  // If the above translate and rotation return to it's origin point, then all clusters have returned to it's starting point.
  rotateSakeenaCircleStage1(incomingsakeenaCircle);

  hypotenuse = dist(incomingsakeenaCircle.transX, incomingsakeenaCircle.transY, incomingsakeenaCircle.sakeenaCircleX,  incomingsakeenaCircle.sakeenaCircleY);
  // fill ('rgba(0,255,0, .01)')
  stroke(0, 0, 0, 15)
  fill(255, 255, 255, 15);
  circle(incomingsakeenaCircle.transX, incomingsakeenaCircle.transY, hypotenuse*2 + incomingsakeenaCircle.sakeenaCircleHeight);
  pop();
}

// This function is your initial Dreamcatcher with the circles spinning at each collective center.
function rotateSakeenaCircleStage1(incomingsakeenaCircle) {
  stroke(0, 0, 0, 50)
  fill(255, 255, 255, 5);

  push();
  // Your original translate was transX of 300 and transY of 200
  // These were adjusted per each cluster. Each cluster was given their own respective transX and transY.
  translate(incomingsakeenaCircle.transX, incomingsakeenaCircle.transY);
  rotate(sakeenaAngle);
  circle (incomingsakeenaCircle.sakeenaCircleX-incomingsakeenaCircle.transX, incomingsakeenaCircle.sakeenaCircleY-incomingsakeenaCircle.transY, incomingsakeenaCircle.sakeenaCircleHeight);
  pop();
  sakeenaAngle = sakeenaAngle + sakeenaSpeed;
}
